package examples;

public class Person {
	private String lastName ="";
	private String firstName="";
	private int age=0;
	private double height=0.0;
	
	public Person(String lastName, String firstName, int age, double height){
		this.lastName = lastName;
		this.firstName = firstName;
		this.age=age;
		this.height=height;
	}
	

}
