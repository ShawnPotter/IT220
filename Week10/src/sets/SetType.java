package sets;
/*
 * An enumerated type is used for variables that should only hold a restricted set of values.
 * Here a Set can only be constructed as a Tree  (TreeSet) or as a Hash 
 * (HashSet)
 * 
 * You can declare variables as type SetMapType
 * SetType type = SeType.TREE
 */
public enum SetType {
      TREE,
      HASH
}
